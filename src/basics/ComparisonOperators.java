package basics;

public class ComparisonOperators {

    public static void main(String[] args) {
        int x = 1;
        int y = 2;

        // Duplicate line with command+D
        System.out.println(x == y);
        System.out.println(x != y);

        System.out.println(x > y);
        System.out.println(x < y);

        System.out.println(x >= y);
        System.out.println(x <= y);
    }

}
